WHERE ((DATA_HORA < DATE_SUB(NOW(), INTERVAL 2 HOUR) AND CONGELA_ATE IS NULL) OR(CONGELA_ATE < DATE_SUB(NOW(), INTERVAL 2 HOUR)));
